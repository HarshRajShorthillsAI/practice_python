print("Hello, \nWorld!")

print("Name:\tJohn")

print("This is a backslash: \\")
# Output: This is a backslash: \

print('It\'s a sunny day.')
# Output: It's a sunny day.

print("She said, \"Hello!\"")
# Output: She said, "Hello!"

print("Hello World!\rHi")
# Output: Hi World!

print("Hello\fWorld!")
# Output: Hello
#         World!

print("\101")  # Output: A

print("\x41")  # Output: A

print("\u03A9")  # Output: Î© (Omega symbol)

raw_string = r"C:\Users\Name\Documents"
print("raw_string: ",raw_string)  # Output: C:\Users\Name\Documents