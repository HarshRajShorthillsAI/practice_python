x += 3  # Equivalent to: x = x + 3
print(x)  # Output: 8

x = 7
x *= 2  # Equivalent to: x = x * 2
print(x)  # Output: 14

x = 2
x **= 3  # Equivalent to: x = x ** 3
print(x)  # Output: 8

x = 10
x %= 3  # Equivalent to: x = x % 3
print(x)  # Output: 1

x = 6  # Binary: 110
x &= 3  # Equivalent to: x = x & 3 (Binary 011)
print(x)  # Output: 2 (Binary 010)

x = 20  # Binary: 10100
x >>= 2  # Equivalent to: x = x >> 2
print(x)  # Output: 5 (Binary 101)

x = 5  # Binary: 101
x ^= 3  # Equivalent to: x = x ^ 3 (Binary 011)
print(x)  # Output: 6 (Binary 110)

